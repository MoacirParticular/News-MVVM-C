import UIKit

public extension UIColor {
    @objc
    static var backgroundColorDefault: UIColor {
        return UIColor(named: "backgroundColorDefault")!
    }
    
    @objc
    static var textLabelColorDefault: UIColor {
        return UIColor(named: "textLabelColorDefault")!
    }
    
}
